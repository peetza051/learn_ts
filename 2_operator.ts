//math op said number
console.log(1+1)
console.log(5*5)
console.log(28/4)
console.log(10%4)
console.log(9**3) //ยกกำลัง


//logic op said boolen
console.log(5 > 2)
console.log(5 == 5)
console.log(5 != 5) // != ไม่เท่ากับ
console.log("5 = '5'",5 == "5")
console.log("5 === '5'",5 === "5") // === เป็นการเปรียบเทียบ data type ของ opject ที่ได้รับ
console.log("5 != '5'",5 != "5")
console.log("5 !== '5'",5 !== "5") //พยายามใช้ === !== เพื่อความปลอดภัยของ Code 


//connection op  
console.log(5 > 4 && 10 < 5)  //&& และ   || หรือ ตะกะศาสตร์


//concat op
console.log(1 + 'hello') //เอาเลขต่อกับข้อความ ใช้ได้แค่การบวก + อย่างเดียว
console.log(1-"hello")
console.log("hi" + "hello")

